#include<iostream>
using namespace std;
int main()
{
    int ch;
    cout<<"Enter a day number"<<endl;
    cin>>ch;
    if(ch==1)
    {
        cout<<"Monday";
    }
    else if(ch==2)
    {
        cout<<"Tuesday";
    }
    else if(ch==3)
    {
        cout<<"Wednesday";
    }
    else if(ch==4)
    {
        cout<<"Thursday";
    }
    else if(ch==5)
    {
        cout<<"Friday";
    }
    else if(ch==6)
    {
        cout<<"Saturday";
    }
    else if(ch==7)
    {
        cout<<"Sunday";
    }

}
